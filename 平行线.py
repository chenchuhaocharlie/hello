import turtle
d=50
turtle.pu()
turtle.pencolor("blue")
turtle.bgcolor("black")
turtle.screensize(300*500)
turtle.pensize(5)
turtle.pd()
turtle.fd(100)
turtle.pencolor("red")
turtle.pu()
turtle.sety(0-d)
turtle.bk(100)
turtle.pd()
turtle.fd(100)
turtle.pencolor("yellow")
turtle.pu()
turtle.sety(0-d-d)
turtle.bk(100)
turtle.pd()
turtle.fd(100)
turtle.done()